package com.second.se;

public class TypeConversion {
	public static void main(String args[]) {
		/*implicit conversion 
		 * integer to float value conversion
		 */
	int a=20;
	float b=a;
	System.out.println(b);
	/*
	 * Explicit conversion 
	 * double value to integer value 
	 */
	double c=30.5;
	int d=(int)c;
	System.out.println(d);
	
		
	}

}
